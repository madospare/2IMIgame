<h3>Lovverk</h3>

<h4>Personopplysningsloven</h4>
<p>Etter personopplysningsloven § 48 kan forsettlige eller grovt uaktsomme overtredelser av de der angitte bestemmelsene straffes med bøter eller fengsel inntil ett år eller begge deler.</p>

<h4>Offentlighetsloven</h4>
<p>Den som forsettlig bryter bestemmelser om offentlighet i denne lov eller i medhold av denne lov straffes med bøter. Dersom lovbruddet er forøvet av en offentlig ansatt tjenestemann, vil man også kunne idømmes tap av stilling eller tjeneste.</p>

<h4>Sikkerhetsloven</h4>
<p>Med bot eller fengsel inntil 3 år straffes den som forsettlig eller grovt uaktsomt offentlig setter frem en diskriminerende eller hatefull ytring. Som ytring regnes også bruk av symboler. Den som i andres nærvær forsettlig eller grovt uaktsomt fremsetter en slik ytring overfor en som rammes av denne, jf. annet ledd, straffes med bot eller fengsel inntil 1 år.</p>

<h4>Arbiedsmiljøloven</h4>
<p>Arbeidstaker skal:</p>
<p>a. bruke påbudt verneutstyr, vise aktsomhet og ellers medvirke til å hindre ulykker og helseskader,</p>
<p>b. straks underrette arbeidsgiver og verneombudet og i nødvendig utstrekning andre arbeidstakere når arbeidstakeren blir oppmerksom på feil eller mangler som kan medføre fare for liv eller helse, og vedkommende ikke selv kan rette på forholdet,</p>
<p>c. avbryte arbeidet dersom arbeidstaker mener at det ikke kan fortsette uten å medføre fare for liv eller helse,</p>
<p>d. sørge for at arbeidsgiver eller verneombudet blir underrettet så snart arbeidstaker blir kjent med at det forekommer trakassering eller diskriminering på arbeidsplassen,</p>
<p>e. melde fra til arbeidsgiver dersom arbeidstaker blir skadet i arbeidet eller pådrar seg sykdom som arbeidstaker mener har sin grunn i arbeidet eller forholdene på arbeidsstedet,</p>
<p>f. medvirke ved utarbeiding og gjennomføring av oppfølgingsplaner ved helt eller delvis fravær fra arbeidet på grunn av ulykke, sykdom, slitasje eller lignende,</p>
<p>g. delta i dialogmøte etter innkalling fra arbeidsgiver, jf. § 4-6 fjerde ledd,</p>
<p>h. rette seg etter påbud fra Arbeidstilsynet.</p>

